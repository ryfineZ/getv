export function PlatformBadges({ activePlatform, onPlatformClick }: PlatformBadgesProps) {
  const platforms: Platform[] = ['youtube', 'tiktok', 'twitter', 'instagram', 'douyin', 'xiaohongshu', 'wechat'];

  return (
    <div className="flex flex-wrap justify-center gap-3 mt-8">
      {platforms.map((platform) => {
        const config = PLATFORMS[platform];
        const isActive = activePlatform === platform;
        const isClickable = platform === 'wechat';

        return (
          <button
            key={platform}
            onClick={() => onPlatformClick?.(platform)}
            disabled={!isClickable}
            className={`flex items-center gap-2 px-4 py-2 rounded-full transition-all duration-300 ${
              isActive
                ? 'scale-110 shadow-lg'
                : 'opacity-60 hover:opacity-100'
            } ${isClickable ? 'cursor-pointer' : 'cursor-default'}`}
            style={{
              backgroundColor: isActive ? config.color : 'rgba(39, 39, 58, 0.5)',
              color: '#fff',
            }}
          >
            {PlatformIcons[platform]}
            <span className="text-sm font-medium">{config.name}</span>
            {isClickable && (
              <span className="text-xs opacity-70">点击查看教程</span>
            )}
          </button>
        );
      })}
    </div>
  );
}
